    public String CrearDescripcion(Cancion rola)
    {
        String artista= rola.getArtista();
        String album= rola.getAlbum();
        int anio= rola.getAnio();
        
        String descripcion= artista+" - "+ album+" ("+anio+")";
        return descripcion;
    }
    
    public void SetImageLabel(JLabel label, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon = new ImageIcon(image.getImage().getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_DEFAULT));
        label.setIcon(icon);
        this.repaint();
    }


